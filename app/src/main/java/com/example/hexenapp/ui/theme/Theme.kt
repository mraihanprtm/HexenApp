@Composable
fun HexenAppTheme(
    darkTheme: Boolean = isSystemInDarkTheme(), // Mengikuti tema sistem secara default
    dynamicColor: Boolean = true, // Mengaktifkan warna dinamis (Material You) di Android 12+
    content: @Composable () -> Unit
) {
    val colorScheme = when {
        dynamicColor && Build.VERSION.SDK_INT >= Build.VERSION_CODES.S -> {
            val context = LocalContext.current
            if (darkTheme) dynamicDarkColorScheme(context) else dynamicLightColorScheme(context)
        }
        darkTheme -> DarkColors
        else -> LightColors
    }

    // Mengatur tampilan status bar dan navigation bar agar sesuai dengan tema
    val view = LocalView.current
    if (!view.isInEditMode) { // Hanya berjalan saat runtime, bukan di mode preview
        SideEffect {
            val window = (view.context as Activity).window
            window.statusBarColor = colorScheme.primary.toArgb() // Atau colorScheme.background.toArgb()
            WindowCompat.getInsetsController(window, view).isAppearanceLightStatusBars = !darkTheme
            // Untuk navigation bar, jika ingin transparan atau sewarna dengan background:
            // window.navigationBarColor = colorScheme.background.toArgb()
            // WindowCompat.getInsetsController(window, view).isAppearanceLightNavigationBars = !darkTheme
        }
    }

    MaterialTheme(
        colorScheme = colorScheme,
        typography = AppTypography, // Menggunakan tipografi yang sudah didefinisikan
        shapes = AppShapes, // Menggunakan bentuk yang sudah didefinisikan
        content = content
    )
}
